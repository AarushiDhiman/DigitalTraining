package com.mindsprint.project1.ConditionalStatement;

public class Conditional {
    public static void main(String[] args) {
        int num1=10;
        int num2=20;
        System.out.println("num1>num2:"+(num1>num2)); //False
        System.out.println("num1<num2:"+(num1<num2)); //True
        System.out.println("num1>=num2:"+(num1>=num2)); //False
        System.out.println("num1<=num2:"+(num1<=num2)); //True
        System.out.println("num1==num2:"+(num1==num2)); //False
        System.out.println("num1!=num2:"+(num1!=num2)); //True
    }
}
